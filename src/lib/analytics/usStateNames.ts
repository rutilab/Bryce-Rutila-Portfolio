export function labelUsState(code: string | null | undefined): string {
  if (!code || /^unknown$/i.test(code.trim())) return 'Unknown';
  const c = code.trim().toUpperCase();
  if (c.startsWith('US-')) {
    const sub = c.slice(3);
    return US_STATE_NAMES[sub] ?? sub;
  }
  return US_STATE_NAMES[c] ?? c;
}
